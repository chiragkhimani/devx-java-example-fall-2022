package com.java.class03;

public class DataTypesPractice {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        long num3 = 128;
        float num4 = 10.27f;
        double num5 = 10.27d;
        double num6 = 20.23; //(d is optional)
        System.out.println(num5 + num6);
        char ch1 = 't';
        char ch2 = 'd';
        String str = "This is my java program";
        boolean b1 = true;
        boolean b2 = false;
        System.out.println(str);
        System.out.println(b1);
    }
}
